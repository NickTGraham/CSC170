			<h3>Synopsis</h3>
			<p>Marissa Mayer, born on May 30, 1975, in Wausau, Wisconsin, was appointed CEO of Yahoo in 2012. Prior to joining Yahoo, she spent 13 years at Google, where her work in product development largely contributed to the site's unique look and feel. At the time of Mayer's Yahoo appointment, she was one of <em>only 20</em> female CEOs in charge of a Fortune 500 company.</p>

			<h3>Early Life</h3>
			<p>Born in Wausau, Wisconsin, on May 30, 1975, Marissa Ann Mayer has described her early years as a small-town childhood, complete with ballet lessons, ice skating, Brownies and debate team competitions. The daughter of an engineer and an art teacher, Mayer demonstrated an early affinity for math and science. While at Wausau West High, she worked at a local grocery store, where she memorized the number codes for produce items in order to streamline the checkout process.</p>
			<p>Although she grew up thinking she would be a doctor, while at Stanford University, Mayer developed a passion for computers and went on to earn both a Bachelor of Science in symbolic systems and an Master of Science in computer science, with a specialization in artificial intelligence. But it was perhaps her research internships at SRI International in Menlo Park, California, and the UBS research lab in Zurich that helped her net 14 job offers fresh out of grad school.</p>

			<h3>Professional Success</h3>
			<p>Mayer's 14th job offer came from Google founders Larry Page and Sergey Brin, who quizzed her on artificial intelligence while sitting at a ping-pong table that the company used for conferences. In 1999, Google wasn't yet an Internet search giant. In fact, the company had <strong>only</strong> 19 employees. Upon accepting an offer to lead Google's user interface and Web server teams, Mayer became the company's 20th employee and its first female engineer.</p>
			<p>Her tenure at Google involved work on some of the company's most recognizable and successful products, including Google Maps, Google Earth, Street View, Google News and Gmail. Known as a fashionista with an eye for design, Mayer is widely credited for the unique look and feel that has come to characterize the Google experience. For example, she was responsible for approving each "doodle" (the custom logos commemorating holidays and events) appearing on the Google home page.</p>
			<p>Mayer spent more than a decade at Google accumulating accolades for her work ethic, eye for detail and vision. In a 2008 interview, however, she seemed to be looking ahead to her next act. <em>"I helped build Google,"</em> Mayer said, <em>"but I don't like to rest on [my] laurels. I think the most interesting thing is what happens next."</em></p>
