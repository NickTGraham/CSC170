			<h3>Yahoo Controversy</h3>
			<p>Eric Jackson of Forbes wrote the following about Mayer's recent finances:</p>
			<p>Mayer is not the first CEO to negotiate a jaw-dropping lucrative compensation package from a board of directors and she certainly won’t be the last.  I can’t really blame her for asking for the moon in compensation and being granted it.  I am very disappointed in the stumbling decline of value of Yahoo’s core business however.</p>
			<p>I’m also highly disappointed that – given the <strong>terrible</strong> results of the core – Mayer has been aggressively selling down her vested stock options every two weeks since February.  In total, she’s sold almost $12 million in stock options since February.  You can find all the transactions <a href="http://www.sec.gov/cgi-bin/browse-edgar?action=getcompany&amp;CIK=0001550608&amp;type=&amp;dateb=&amp;owner=only&amp;count=100">here</a>, but I will summarize.</p>
			<ul>
				<li>On August 5th, she sold $620k in stock</li>
				<li>On July 25th, she sold $540k in stock</li>
				<li>On July 14th, she sold $580k in stock</li>
				<li>On June 30th, she sold $530k in stock</li>
				<li>On June 12th, she sold $620k in stock</li>
				<li>On June 3rd, she sold $570k in stock</li>
				<li>On May 19th, she sold $540k in stock</li>
				<li>On May 2nd, she sold $610k in stock</li>
				<li>On April 17th, she sold $530k in stock</li>
				<li>On April 3rd, she sold $630k in stock</li>
				<li>On March 19th, she sold $720k in stock</li>
				<li>On March 6th, she sold $740k in stock</li>
				<li>On March 3rd, she sold $3.06m in stock</li>
				<li>On February 11th, she sold $650k in stock</li>
			</ul>
			<p>In total, she’s sold <strong>$11.6 million</strong> in stock in the last six months.  All of these sales have been done under something called the SEC Rule 10b5-1.  It’s a system under which executives can sign up in advance to sell a predetermined number of shares (or options) at a predetermined price.  Because it’s all done in advance, you don’t run into the problems that Steve Jobs and others had 10 years ago when they were accused of back-dating stock options to get the most favorable stock prices for themselves</p>
			<p>So, Mayer’s sales are certainly legal but they aren’t right in my view.  I’ve had some financial advisors tell me that they think it’s okay for CEOs and executives to sell their stock holdings aggressively because so much of their total net worth is tied up in the stock that this allows for better <em>“diversification.”</em>  If I was a CEO with a $200 million comp package, I’d certainly expect my financial advisor to tell me this was perfectly fine.</p>
			<p>But I don’t buy this argument.  As an investor – and if I was an employee – I want all a CEO’s net worth tied up in a company.  I want them to feel like their future wealth and reputation depends on their current company being really successful.  I think it sends the wrong message to employees to be selling $650,o00 in stock every two weeks for walking around money in your pocket.</p>
			<p>And given the terrible results of the core business, if anything, I believe Mayer should be digging into her own pocket and buying Yahoo stock, not selling it.  If you believe in the “daily habits” strategy for Yahoo, then shout it from the rooftops and put your own money where your mouth is.<a href="http://www.forbes.com/sites/ericjackson/2014/08/17/marissa-mayers-compensation-and-stock-selling-not-linked-to-performance/">[2]</a></p>

